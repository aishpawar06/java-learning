public class Variables {
    public static void main(String args[]){
        int var1 = 10;
        int var2 = 20;
        String var3 = "Tony";
        System.out.println("var1 = "+var1);
        System.out.println("var2 = "+var2);
        System.out.println("var3 = "+var3);
        var1 = 90;
        System.out.println("var1 after update = "+ var1);
    }
}
