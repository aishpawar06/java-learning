public class DataTypes {
    public static void main(String args[]){
        byte b = 8; //range is upto -128 to +127
        System.out.println("byte = "+b);
        boolean var = true;
        System.out.println("Boolean = "+var);
        float price = 10.1f;
        System.out.println("Float = "+price);
        int number = 10;
        System.out.println("integer = "+number);
        char ch='a';
        System.out.println("characher = "+ ch);
        //long is used for big integers
        long l = 10000000;
        System.out.println("long = "+l);
        //double is used for high precision floating point numbers
        double d = 3.1414232423;
        System.out.println("Double = "+ d);
        short s = 1000;
        System.out.println("Short = "+s);
    }
}
